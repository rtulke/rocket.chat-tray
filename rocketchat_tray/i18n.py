_CATALOGS = {"de": _DE, "en": _EN, "fr": _FR, "it": _IT, "es": _ES}


def tr(key: str, **kwargs) -> str:
    """Look up `key` in the detected system language, falling back to
    English and then to the key itself so a missing translation never
    crashes the app -- just shows something less polished."""
    catalog = _CATALOGS.get(LANGUAGE, _CATALOGS[DEFAULT_LANGUAGE])
    text = catalog.get(key) or _CATALOGS[DEFAULT_LANGUAGE].get(key) or key
    return text.format(**kwargs) if kwargs else text
